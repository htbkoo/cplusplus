struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int maxPathSum(TreeNode* root) {
        answer = numeric_limits<int>::min();
        
        findBest(root);
        
        return answer;
    }
    
private:
    int answer;
    int findBest(TreeNode* node) {
        if (node) {
            int left = findBest(node->left);
            int right = findBest(node->right);
            
            answer = max(answer, node->val + left + right);
            
            return max(0, node->val + max(left, right));
        } else {
            return 0;
        }
    }
};
