class MyHashMap {
public:
    vector<int> arr;
    MyHashMap() {
        arr = vector<int>(1e6 + 1, -1);
    }
    
    void put(int key, int value) {
        arr[key] = value;
    }
    
    int get(int key) {
        return arr[key];
    }
    
    void remove(int key) {
        arr[key] = -1;
    }
};

