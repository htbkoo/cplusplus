struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    TreeNode* bstToGst(TreeNode* root) {
        // TODO: assume we can mutate the given tree, otherwise we should clone the tree beforehand
        addSum(root, 0);

        return root;
    }

private:
    int addSum(TreeNode* root, int prevSum) {
        if (root == nullptr) {
            return prevSum;
        }

        auto result = addSum(root->right, prevSum);
        result += root->val;
        root->val = result;
        if (root->left) {
            result = addSum(root->left, result);
        }
        return result;
    }
};
